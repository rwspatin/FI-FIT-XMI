package tabelaxml;

/**
 *
 * @author rwspa
 */
public class Complemento {
    private int lowerValor;
    private int upperValor;

    public Complemento() {
    }
    
    /**
     * @return the lowerValor
     */
    public int getLowerValor() {
        return lowerValor;
    }

    /**
     * @param lowerValor the lowerValor to set
     */
    public void setLowerValor(int lowerValor) {
        this.lowerValor = lowerValor;
    }

    /**
     * @return the upperValor
     */
    public int getUpperValor() {
        return upperValor;
    }

    /**
     * @param upperValor the upperValor to set
     */
    public void setUpperValor(int upperValor) {
        this.upperValor = upperValor;
    }
    
    
}
